  private void loadUserData() {
    DefaultTableModel tableModel = (DefaultTableModel) userTable.getModel();
    tableModel.setRowCount(0);
    List<String[]> users = administrator.viewUsers();

    for (String[] user : users) {
      tableModel.addRow(user);
    }
  }

